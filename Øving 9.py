class Avtale:
  def __init__(self, tittel, sted, start, varighet):
    self.tittel = tittel
    self.sted = sted
    self.start = start
    self.varighet = varighet



 
# -----------------------------------------------------------
#                     Oppgave e
# -----------------------------------------------------------

def __str__(self):
    return f"{self.tittel}{self.sted}{self.start}{self.varighet}"         


